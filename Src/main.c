/* Timer IC Interrupt Service Rountie */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  if(htim->Instance==TIM2) // Interrupt come from Timer2
  {
     /* Boi vi cau hinh sinh ngat "BOTH EDGE INPUT CAPTURE" cua timer 2 */
    /* Cu moi khi tin hieu can do doi trang thai thi se sinh ra ngat input capture va nhay vao day */
    
    count++;
    if(count==1)
    {
      capture_value1= __HAL_TIM_GET_COMPARE(&htim2,TIM_CHANNEL_1);
      if(HAL_GPIO_ReadPin(GPIOA,GPIO_PIN_5))
      {
        state=1;
      }else
      {
        state=0;
      }
    }else if(count==2)
    {
      count=0;
      capture_value2= __HAL_TIM_GET_COMPARE(&htim2,TIM_CHANNEL_1);
      
      /* Tan so that cua tin hieu do duoc */
      frequency=84000000.0/capture_value2;
      
      /* Day la gia tri duty that cua tin hieu do duoc (%) */
      measure_duty=((float)capture_value1/capture_value2)*100;
      
      /* Note: Cong thuc tren chi dung khi capture_value1 la gia tri doc duoc khi
      tin hieu o muc "1", neu khong thi phai doi nguoc lai nhu sau */
      if(state==1)
      {
        measure_duty=100-measure_duty;
      }
      __HAL_TIM_SET_COUNTER(&htim2,0);
    }   
  }
}


